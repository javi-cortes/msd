    //bot√≥n seleccionar otro 
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JFileChooser jFC = new JFileChooser();
        File f = null;
        try {
            f = new File(new File(".").getCanonicalPath());
        } catch (IOException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
        }
        jFC.setCurrentDirectory(f);
        int respuesta = jFC.showOpenDialog(this);
        if (respuesta == JFileChooser.APPROVE_OPTION)
        {
            File archivoSeleccionado = jFC.getSelectedFile();
            jTextField2.setText(archivoSeleccionado.getName());
            simulador.setNombreFichero(archivoSeleccionado.getName(), true); // true = modificamos el nombre del fichero manualmente
        }
    }//GEN-LAST:event_jButton3ActionPerformed

